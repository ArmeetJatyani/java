package com.codefortomorrow.beginner.chapter6.examples;

public class RelationalOperators {
	public static void main(String[] args) {
		int radius = 5;

		System.out.println(radius < 0); // false
		System.out.println(radius <= 0); // false
		System.out.println(radius > 0); // true
		System.out.println(radius >= 0); // true
		System.out.println(radius == 0); // false
		System.out.println(radius != 0); // true
	}
}
